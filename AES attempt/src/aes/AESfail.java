	public String aESEncrypt(char[] input ){
		this.message = input;
		
		char[] testing = new char[16];
		for(int i = 0; i < 16; i++){
			testing[i] = input[i];
		}
		int numOfRounds = 9;
		
		//key expansion
		char[] expandedKey = new char[176];
		keyExpansion(expandedKey, key);
		addRoundKey(testing, key);
		for(int i = 0; i < numOfRounds; i ++){
			subBytes(testing);//null for now to avoid red lines
			shiftRows(testing);
			mixColumns(testing);
			addRoundKey(testing, expandedKey+(16*(i+1)));
		}
		//final round
		subBytes(null);
		shiftRows(null);
		addRoundKey(null, null);
		return null;
	}

